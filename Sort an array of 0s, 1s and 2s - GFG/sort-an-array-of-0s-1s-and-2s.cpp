class Solution
{
    public:
    void sort012(int a[], int n)
    {
        map<int,int> m1;
        for(int i = 0;i < n; i++){
            m1[a[i]]++;
        }
        int j = 0;
        for(auto &it:m1){
            for(int i = 0;i<it.second;i++)  a[j++] = it.first;
        }
    }
    
};

